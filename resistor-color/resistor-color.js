export const colorCode = (color) => {
  let c = color.toLowerCase();

  if (c == 'black') return (0);
  if (c == 'brown') return (1);
  if (c == 'red') return (2);
  if (c == 'orange') return (3);
  if (c == 'yellow') return (4);
  if (c == 'green') return (5);
  if (c == 'blue') return (6);
  if (c == 'violet') return (7);
  if (c == 'grey') return (8);
  if (c == 'white') return (9);
};

